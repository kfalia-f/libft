int		ft_atoi(char *str)
{
	int res;
	int i;

	i = 0;
	res = 0;
	while ((*str == ' ') || (*str == '\t'))
		str++;
	if (*str == '-')
		i = 1;
	str++;
	while (*str)
	{
		if (*str == '+')
			str++;
		if ((*str < '0') || (*str > '9'))
			break ;
		res = res * 10 + (*str - '0');
		str++;
	}
	if (i == 1)
		res = res * -1;
	return (res);
}
