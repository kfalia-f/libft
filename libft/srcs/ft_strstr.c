char		*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;
	int k;

	i = 0;
	j = 0;
	while ((str[i] != to_find[0]) && (str[i] != '\0'))
		i++;
	k = i;
	while ((str[i] == to_find[j]) && (str[i] != '\0'))
	{
		j++;
		i++;
	}
	if (to_find[j] == '\0')
		return (&str[k]);
	else
		return (0);
}
